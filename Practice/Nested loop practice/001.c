#include <stdio.h>

int main() {
    int x = -5;


    // Print the incremented value of X
    printf("Incremented X: %d\n", x++);
    printf("Incremented X: %d\n", ++x);

    // Print the decremented value of X
    printf("Decremented X: %d\n", x--);
    printf("Decremented X: %d\n", --x);

    return 0;
}
